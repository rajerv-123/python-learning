a = "31.2";
b = float(a); # converting string to float
print(b)  # Output: 31.2


c = 42;
d = str(c); # converting integer to string
print(d)  # Output: "42"    

e = 3.14;
f = int(e); # converting float to integer
print(f)  # Output: 3

g = True;
h = int(g); # converting boolean to integer
print(h)  # Output: 1

i = None;
j = str(i); # converting NoneType to string
print(j)  # Output: "None"